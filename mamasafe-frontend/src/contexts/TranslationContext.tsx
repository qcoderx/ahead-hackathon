import React, { createContext, useContext, useState, ReactNode } from 'react'

interface Language {
  id: string
  name: string
}

interface TranslationContextType {
  currentLanguage: Language
  setLanguage: (language: Language) => void
  t: (key: string) => string
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined)

const defaultLanguage: Language = { id: 'en', name: 'English' }

export const TranslationProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState<Language>(defaultLanguage)

  const setLanguage = (language: Language) => {
    setCurrentLanguage(language)
    localStorage.setItem('mamasafe-language', JSON.stringify(language))
  }

  const t = (key: string): string => {
    return translations[currentLanguage.id]?.[key] || translations.en[key] || key
  }

  return (
    <TranslationContext.Provider value={{ currentLanguage, setLanguage, t }}>
      {children}
    </TranslationContext.Provider>
  )
}

export const useTranslation = () => {
  const context = useContext(TranslationContext)
  if (!context) {
    throw new Error('useTranslation must be used within TranslationProvider')
  }
  return context
}

